export const TRANSLATION_CATALOGS = Object.freeze({
  en: EN,
  es: buildCatalog({ ...ES, ...(WIZARD_VISIBLE_OVERRIDES.es || {}) }),
  ...Object.fromEntries(Object.entries(QUICK_LOCALE_OVERRIDES).map(([locale, overrides]) => [
    locale,
    buildCatalog({ ...overrides, ...(WIZARD_VISIBLE_OVERRIDES[locale] || {}), ...(EXTENDED_LOCALE_OVERRIDES[locale] || {}) })
  ]))
});

export const REQUIRED_I18N_KEYS = Object.freeze(Object.keys(EN));

export function normalizeLocale(locale = DEFAULT_LOCALE) {
  const raw = String(locale || DEFAULT_LOCALE).trim().toLowerCase().replace("_", "-");
  if (!raw) return DEFAULT_LOCALE;
  if (raw === "es-419") return "es";
  if (raw === "pt-br" || raw === "pt-pt") return "pt";
  if (raw === "fil") return "tl";
  const primary = raw.split("-")[0];
  return LANGUAGE_OPTIONS.some(([code]) => code === primary) ? primary : DEFAULT_LOCALE;
}

export function translate(key, params = {}, locale = DEFAULT_LOCALE) {
  const lang = normalizeLocale(locale);
  const table = TRANSLATION_CATALOGS[lang] || EN;
  let value = table[key] || EN[key] || `[${key}]`;
  for (const [name, replacement] of Object.entries(params || {})) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }
  return value;
}

export function translateText(text, locale = DEFAULT_LOCALE) {
  const raw = String(text ?? "");
  const trimmed = raw.trim();
  if (!trimmed) return raw;
  const key = Object.keys(EN).find((entry) => EN[entry] === trimmed);
  if (!key) return raw;
  return raw.replace(trimmed, translate(key, {}, locale));
}

export function isRTL(locale = DEFAULT_LOCALE) {
  return RTL_LOCALES.has(normalizeLocale(locale));
}

export function applyTranslations(root = document, locale = DEFAULT_LOCALE) {
  const lang = normalizeLocale(locale);
  const rtl = RTL_LOCALES.has(lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = rtl ? "rtl" : "ltr";
  document.documentElement.classList.toggle("rtl", rtl);

  translateTextNodes(root, lang);
  translateAttributes(root, lang);
}

const UNTRANSLATABLE_KEYS = new Set(["appName", "byline", "coDev", "flow", "api", "dom", "discord", "sceneBuilder"]);

export function localeDiagnostics(locale = DEFAULT_LOCALE) {
  const lang = normalizeLocale(locale);
  if (lang === DEFAULT_LOCALE) {
    return { locale: lang, supported: true, translated: REQUIRED_I18N_KEYS.length, total: REQUIRED_I18N_KEYS.length, missing: [], fallback: false };
  }
  const catalog = TRANSLATION_CATALOGS[lang] || {};
  const userFacingKeys = REQUIRED_I18N_KEYS.filter((k) => !UNTRANSLATABLE_KEYS.has(k));
  const missing = userFacingKeys.filter((k) => !catalog[k]);
  return {
    locale: lang,
    supported: LANGUAGE_OPTIONS.some(([code]) => code === lang),
    translated: userFacingKeys.length - missing.length,
    total: userFacingKeys.length,
    missing,
    fallback: !TRANSLATION_CATALOGS[lang]
  };
}

function buildCatalog(overrides) {
  return Object.freeze({ ...overrides });
}

function translateTextNodes(root, locale) {
  if (!root?.createTreeWalker && !document.createTreeWalker) return;
  const owner = root.ownerDocument || document;
  const walker = owner.createTreeWalker(root.body || root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (shouldSkipElement(parent)) return NodeFilter.FILTER_REJECT;
      return node.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  for (const node of nodes) {
    if (!sourceTextByNode.has(node)) sourceTextByNode.set(node, node.nodeValue);
    node.nodeValue = translateText(sourceTextByNode.get(node), locale);
  }
}

function translateAttributes(root, locale) {
  const owner = root.ownerDocument || document;
  const elements = Array.from((root.body || root).querySelectorAll?.("[title],[placeholder],[aria-label]") || []);
  if (root.nodeType === Node.ELEMENT_NODE && root.matches?.("[title],[placeholder],[aria-label]")) {
    elements.unshift(root);
  }
  for (const element of elements) {
    if (shouldSkipElement(element)) continue;
    for (const attr of ["title", "placeholder", "aria-label"]) {
      const value = element.getAttribute(attr);
      if (!value) continue;
      const sourceAttr = `data-i18n-source-${attr}`;
      if (!element.hasAttribute(sourceAttr)) element.setAttribute(sourceAttr, value);
      element.setAttribute(attr, translateText(element.getAttribute(sourceAttr), locale));
    }
  }
}

function shouldSkipElement(element) {
  return Boolean(element.closest?.([
    ".material-symbols-outlined",
    ".material-symbols-rounded",
    ".material-icons",
    ".log-display",
    ".log-pre",
    ".q-prompt",
    ".assign-prompt",
    ".g-prompt",
    ".scene-clip-prompt",
    "textarea",
    "input",
    "pre",
    "code",
    "[data-no-i18n]"
  ].join(",")));
}
